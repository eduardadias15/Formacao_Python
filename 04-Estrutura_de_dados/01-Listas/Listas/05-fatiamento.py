lista = ["f", "o", "r", "m", "a", "c", "a", "o"]

print(lista[2:])  # ["r", "m", "a", "c"]
print(lista[:2])  # ["f", "o"]
print(lista[1:3])  # ["o", "r"]
print(lista[0:3:2])  # ["f", "r"]
print(lista[::])  # ["f", "o", "r", "m", "a", "c", "a", "o"]
print(lista[::-1])  # ["o", "a", "c", "a", "m", "r", "o", "f"]