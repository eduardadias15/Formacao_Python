# Tuplas são estruturas de dados imutáveis, ou seja, não podem ser alteradas após a sua criação.
# Elas são definidas usando parênteses () e os elementos são separados por vírgulas.
# As tuplas podem conter elementos de diferentes tipos, como strings, números e outras tuplas.

frutas = (
    "laranja",
    "pera",
    "uva",
)
print(frutas)

letras = tuple("python")
print(letras)

numeros = tuple([1, 2, 3, 4])
print(numeros)

pais = ("Brasil",)
print(pais)